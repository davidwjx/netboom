#ifdef SB_ALLOCATOR_DMARESOURCE
#include "dmares_mgmt.h"    // DMAResource_Init
#include "dmares_buf.h"     // DMAResource_Alloc

static int DMAResourceInit = 0;
static int DMAHandleCount = 0;
static DMAResource_Handle_t DMAHandles[3];

void FreeDMAResources(void)
{
    while (DMAHandleCount > 0)
    {
        DMAResource_Release(DMAHandles[--DMAHandleCount]);
        c_memset(DMAHandles[DMAHandleCount], 0, sizeof DMAHandles[DMAHandleCount]);
    }

    if (DMAResourceInit)
    {
        DMAResource_UnInit();
    }
    DMAResourceInit = 0;
}

void DMAResourcesInit(void)
{
    if (DMAResourceInit == 0)
    {
        if (!DMAResource_Init())
        {
            fail("Failed to initialize DMAResource");
        }
    }
    DMAResourceInit = 1;
}

void ConvertVectorToDMAResource(SBIF_SGVector_t *vec)
{
    DMAResource_Properties_t Props = { 0, 0, 0, 0 };
    DMAResource_AddrPair_t AddrPair;
    int res;

    Props.Size = vec->DataLen;
    Props.Alignment = 4;

    DMAResourcesInit();

    res = DMAResource_Alloc(Props, &AddrPair, &DMAHandles[DMAHandleCount]);
    fail_if (res != 0, "Failed to allocate DMA buffer");

    /* Copy input data. */
    c_memcpy(AddrPair.Address.Native_p, vec->Data_p, vec->DataLen);

    /* Set vec to new DMA-able address. */
    vec->Data_p = AddrPair.Address.Native_p;

    /* Increment handle count. */
    DMAHandleCount++;
}

#else
void FreeDMAResources(void)
{
    /* Not used because DMA resources are not enabled. */
}

void ConvertVectorToDMAResource(SBIF_SGVector_t *vec)
{
    /* Not used because DMA resources are not enabled. */
}
#endif

/* Equivalent of ConvertVectorToDMAResource with input expressed as
   memory area. */
void ConvertAreaToDMAResource(void **Area_pp, size_t sz)
{
    SBIF_SGVector_t vec;
    vec.Data_p = *Area_pp;
    vec.DataLen = sz;
    ConvertVectorToDMAResource(&vec);
    *Area_pp = vec.Data_p;
}

void SB_SW_ECDSA_Verify_Image_p224(SB_Result_t expect)
{
    SBIF_SGVector_t vec[1];
    SB_Result_t res;
    uint32_t storage[SBLIB_CFG_STORAGE_SIZE / sizeof(uint32_t)];

    const SBIF_ECDSA_Header_t * const header_p =
        (const SBIF_ECDSA_Header_t *) test_array224_aligned;

    c_memcpy(test_array224_aligned, test_array224_in, sizeof(test_array224_aligned));

    vec[0].Data_p = (uint32_t *)(((char *) (test_array224_aligned)) + sizeof(*header_p));
    vec[0].DataLen = sizeof(test_array224_aligned) - sizeof(*header_p);

    ConvertAreaToDMAResource((void **) &header_p, sizeof(*header_p));
    ConvertVectorToDMAResource(vec);
    res = SB_ECDSA_Image_DecryptAndVerify((void *)&storage, NULL,
                                          &test_pk224, NULL,
                                          header_p, vec, vec, 1);

    FreeDMAResources();
    fail_if(res != expect,
            "Image verification result comparison failure: %d (expected=%d)",
            (int) res, (int) expect);
}

